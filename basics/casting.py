# Number to String:
random_number = 18
print(type(random_number))
random_string = str(random_number)
print(type(random_string))

# String to Integer:
random_string = "18"
print(type(random_string))
random_integer = int(random_string)
print(type(random_integer))

# String to Float:
random_string = "02.18"
print(type(random_string))
random_float = float(random_string)
print(type(random_float))